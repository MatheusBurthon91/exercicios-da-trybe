// fazer um programa que ao declarar uma variavel, imprima positive, se o valor for positivo, negative se for negativo
// e imprima zero se for zero.
let numero = 0;

if (numero > 0) {
    console.log('positive');
} else if (numero < 0) {
    console.log('negative');
} else if ( numero === 0) {
    console.log('zero');
}
